<script lang="ts">
import { ref } from 'vue';

export default {
  Name: 'PlayOffline',
  setup() {
    const step = ref(1);
    function nextStep() {
      step.value++;
    }
    function prevStep() {
      if (step.value > 1) {
        step.value--;
      }
    }
    return {
      step,
      nextStep,
      prevStep,
    };
  },
};
</script>

