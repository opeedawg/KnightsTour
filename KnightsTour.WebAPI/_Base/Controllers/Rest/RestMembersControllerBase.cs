        /// <param name="arguments">A dynamic list of arguments (required by the wrapping method call 'ExecuteCommon()').</param>
        /// <returns>A <seealso cref="System.Web.Http.IActionResult" /> result.</returns>
        private IActionResult _EventHistories(dynamic[] arguments)
        {
            WebApiCallLog callLog = (WebApiCallLog)arguments[0];

            if (callLog.IsAuthorized())
            {
                KnightsTour.MemberLogic logic = new KnightsTour.MemberLogic(callLog.UserName);
                KnightsTour.Member member = logic.GetById(callLog.Arguments[0]);
                if (member == null)
                {
                    return new NotFoundResult();
                }
                else
                {
                    if (callLog.Arguments.Length == 1)
                    {
                        return Ok(ApplyCollectionFilter<KnightsTour.EventHistory>(Request, member.EventHistories));
                    }
                    else //A request for a specific child object has been entered.
                    {
                        KnightsTour.EventHistory childEventHistory = member.EventHistories.FirstOrDefault(p => p.Id == callLog.Arguments[1]);
                        if (childEventHistory != null)
                        {
                            return Ok(childEventHistory);
                        }
                        else
                        {
                            return new NotFoundResult();
                        }
                    }
                }
            }
            else
            {
                return ProcessExceptions(callLog);
            }
        }
