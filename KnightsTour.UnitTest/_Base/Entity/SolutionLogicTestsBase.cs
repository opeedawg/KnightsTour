                }

                // If the primary key insertion type is manual, then configure some values now.
                if (solutions[0].PKInsertConfiguration == KnightsTour.CoreLibrary.Enumerations.InsertPKRule.Manual)
                {
                    foreach (KnightsTour.Solution solution in solutions)
                    {
                        solution.Id = GetRandomValue<int?>();
                    }
                }

                KnightsTour.CoreLibrary.IActionResponse response = logic.Insert(solutions, 500);
                Assert.IsTrue(response.IsValid);

                // Block size less than the number of accounts.
                solutions = CreateEntities<KnightsTour.Solution>(BulkTestQuantity);

                Assert.IsNotNull(solutions);
                Assert.IsTrue(solutions.Count == BulkTestQuantity);
                foreach (KnightsTour.Solution solution in solutions)
                {
                    Assert.IsTrue(solution.IsNew);
                }

                // If the primary key insertion type is manual, then configure some values now.
                if (solutions[0].PKInsertConfiguration == KnightsTour.CoreLibrary.Enumerations.InsertPKRule.Manual)
                {
                    foreach (KnightsTour.Solution solution in solutions)
                    {
                        solution.Id = GetRandomValue<int?>();
