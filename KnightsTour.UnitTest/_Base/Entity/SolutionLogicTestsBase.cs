        public void SolutionLogic_Update_Multiple()
        {
            using (var transactionScope = new TransactionScope())
            {
                KnightsTour.SolutionLogic logic = new KnightsTour.SolutionLogic(UserName);

                string pk = new KnightsTour.Solution().PrimaryKeyField;

                if (GetEntityProperty<KnightsTour.Solution, KnightsTour.SolutionLite>(KnightsTour.CoreLibrary.Enumerations.EntityProperty.RecordCount) > 0)
