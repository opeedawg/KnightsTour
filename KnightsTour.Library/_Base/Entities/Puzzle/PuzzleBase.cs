        /// <exception cref="NotImplementedException"></exception>
        public dynamic ToDynamic(KnightsTour.CoreLibrary.Enumerations.DynamicObjectStrategy strategy = KnightsTour.CoreLibrary.Enumerations.DynamicObjectStrategy.AllProperties)
        {
            dynamic dynamicPuzzle = new JObject();
            switch (strategy)
            {
                case KnightsTour.CoreLibrary.Enumerations.DynamicObjectStrategy.AllProperties:
                    dynamicPuzzle.PuzzleId = PuzzleId;
                    dynamicPuzzle.PuzzleCode = PuzzleCode;
                    dynamicPuzzle.BoardId = BoardId;
                    dynamicPuzzle.DifficultyLevelId = DifficultyLevelId;
                    dynamicPuzzle.Path = Path;
                    dynamicPuzzle.PuzzleOfTheDayDate = PuzzleOfTheDayDate;
                    break;
                case KnightsTour.CoreLibrary.Enumerations.DynamicObjectStrategy.AllPropertiesWithOriginals:
                    dynamicPuzzle.PuzzleId = PuzzleId;
                    dynamicPuzzle.PuzzleId_Original = InitialState.PuzzleId;
                    dynamicPuzzle.PuzzleCode = PuzzleCode;
                    dynamicPuzzle.PuzzleCode_Original = InitialState.PuzzleCode;
                    dynamicPuzzle.BoardId = BoardId;
                    dynamicPuzzle.BoardId_Original = InitialState.BoardId;
                    dynamicPuzzle.DifficultyLevelId = DifficultyLevelId;
                    dynamicPuzzle.DifficultyLevelId_Original = InitialState.DifficultyLevelId;
                    dynamicPuzzle.Path = Path;
                    dynamicPuzzle.Path_Original = InitialState.Path;
                    dynamicPuzzle.PuzzleOfTheDayDate = PuzzleOfTheDayDate;
                    dynamicPuzzle.PuzzleOfTheDayDate_Original = InitialState.PuzzleOfTheDayDate;
                    break;
                case KnightsTour.CoreLibrary.Enumerations.DynamicObjectStrategy.ModifiedPropertiesOnly:
                    if (IsModified(Enumerations.PuzzleProperty.PuzzleId))
                    {
                        dynamicPuzzle.PuzzleId = PuzzleId;
                        dynamicPuzzle.PuzzleId_Original = InitialState.PuzzleId;
                    }
                    if (IsModified(Enumerations.PuzzleProperty.PuzzleCode))
                    {
                        dynamicPuzzle.PuzzleCode = PuzzleCode;
                        dynamicPuzzle.PuzzleCode_Original = InitialState.PuzzleCode;
                    }
                    if (IsModified(Enumerations.PuzzleProperty.BoardId))
                    {
                        dynamicPuzzle.BoardId = BoardId;
                        dynamicPuzzle.BoardId_Original = InitialState.BoardId;
                    }
                    if (IsModified(Enumerations.PuzzleProperty.DifficultyLevelId))
                    {
                        dynamicPuzzle.DifficultyLevelId = DifficultyLevelId;
                        dynamicPuzzle.DifficultyLevelId_Original = InitialState.DifficultyLevelId;
                    }
                    if (IsModified(Enumerations.PuzzleProperty.Path))
                    {
                        dynamicPuzzle.Path = Path;
                        dynamicPuzzle.Path_Original = InitialState.Path;
                    }
                    if (IsModified(Enumerations.PuzzleProperty.PuzzleOfTheDayDate))
                    {
                        dynamicPuzzle.PuzzleOfTheDayDate = PuzzleOfTheDayDate;
                        dynamicPuzzle.PuzzleOfTheDayDate_Original = InitialState.PuzzleOfTheDayDate;
                    }
                    break;
                default:
                    throw new NotImplementedException($"{strategy.ToString()} strategy not implemented.");
            }
            return dynamicPuzzle;
        }

